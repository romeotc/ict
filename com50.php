1. เจ้าของเครื่องคอมพิวเตอร์ไม่อนุญาตให้เข้าระบบคอมพิวเตอร์ของเขา ถ้าเราแอบเข้าไป จำคุก 6 เดือน<br>
2. เจาะเข้าระบบคอมพิวเตอร์ของคนอื่น แล้วเผยแพร่ให้คนอื่นรู้  จำคุกไม่เกินปี<br>
3. แอบเข้าไปล้วงข้อมูลส่วนบุคคลที่เก็บเอาไว้ในระบบคอมพิวเตอร์  จำคุกไม่เกิน 2 ปี<br>
