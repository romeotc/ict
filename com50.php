5. ข้อมูลที่อยู่ในระบบคอมพิวเตอร์ ถ้ามีไปตัดต่อ ดัดแปลง จำคุกไม่เกิน 5 ปี (ดังนั้นอย่าไปแก้ไขงานเอกสารที่อยู่ในเครื่องคอมพิวเตอร์คนอื่น)<br>
