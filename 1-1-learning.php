<h3 class="w3-margin-left w3-"> นิยามเกี่ยวกับเทคโนโลยีสารสนเทศ</h3></div>

<p class="w3-xlarge w3-left">&nbsp;&nbsp;&nbsp;&nbsp;คำว่า <b class="w3-text-blue">เทคโนโลยี (technology)</b> หมายถึง การประยุกต์เอาความรู้ทางด้านวิทยาศาสตร์มาใช้ให้เกิดประโยชน์ การศึกษาพัฒนาองค์ความรู้ต่าง ๆ ก็เพื่อให้เข้าใจธรรมชาติ กฎเกณฑ์ของสิ่งต่าง ๆ และหาทางนำมาประยุกต์ให้เกิดประโยชน์  </p>
<center><img src="img/technology2.png" class="w3-round " alt="intro" style="width:50%">
<p>ที่มา(https://rohidassanap.files.wordpress.com/2013/06/technology-web-development.png)
</center>
<p class="w3-xlarge w3-left">&nbsp;&nbsp;&nbsp;&nbsp;คำว่า <b class="w3-text-teal">สารสนเทศ (information)</b> หมายถึง ผลลัพที่เกิดจากการนำข้อมูล (data) มาผ่านกระบวนการต่างๆ อย่างเป็นระบบจนได้สิ่งที่มีประโยชน์มีคุณค่าและสาระ หรือมีเนื้อหาและรูปแบบที่เหมาะสมตามความความต้องการของผู้ใช้ </p>
<center><img src="img/information.jpg" class="w3-round " alt="intro" style="width:50%">
<p>ที่มา(http://www.infogineering.net/wp-content/uploads/2009/08/model.jpg)</center>
<p class="w3-xlarge w3-left">&nbsp;&nbsp;&nbsp;&nbsp;<b class="w3-text-blue">เทคโนโลยี</b><b class="w3-text-teal">สารสนเทศ </b>หมายถึง การนําความรู้ทางด้านวิทยาศาสตร์มาประยุกตไซ้เพื่อสร้าง
หรือจัดการกับสารสนเทศอย่างเป็นระบบและรวดเร็ว โดยอาศัยเทคโนโลยีทางด้าน
คอมพิวเตอร์ ทั้งนี้เพื่อให้เกิดประโยชน์ต่อบุคคล กลุ่มบุคคล หรือองค์กร ทั้งนี้เทคโนโลยี
สารสนเทศยังต้องพึ่งพาเทคโนโลยีด้านการสื่อสารและโทรคมนาคม ซึ่งเป็นวิธีการที่จะส่ง
ข้อมูลจากที่หนึ่งไปยังอีกที,หนึ่ง เพื่อการแลกเปลี่ยนหรือเผยแพร่ข้อมูล และสารสนเทศได้
อย่างรวดเร็วทันต่อการใช้ประโยชน์ผ่านอุปกรณ์สื่อสาร</p>
<p class="w3-xlarge w3-left">
องค์ประกอบของระบบสารสนเทศ (Component of Information System) ประกอบด้วย 5 ส่วน คือ
ฮาร์ดแวร์ ซอฟต์แวร์ บุคลากร ข้อมูลและขั้นตอนการปฏิบัติงาน</p><center>
