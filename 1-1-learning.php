<h3><b>1. ฮาร์ดแวร์</b></h3>
<center>
<img src="img/hardware.jpg" class="w3-round " alt="intro" style="width:60%">
<p>ที่มา(http://kingofwallpapers.com/hardware/hardware-001.jpg)</center>
<p class="w3-xlarge w3-left">&nbsp;&nbsp;&nbsp;&nbsp; ฮาร์ดแวร์ (Hardware) หมายถึง เครื่องมือหรืออุปกรณ์ต่างๆ ที่ใช้งานในระบบสารสนเทศ เช่น อุปกรณ์สำนักงานและเครื่องมืออิเล็กทรอนิกส์ต่างๆ โดยฮาร์ดแวร์ที่สำคัญที่สุดในระบบสารสนเทศ คือ เครื่องคอมพิวเตอร์ ฮาร์ดแวร์ของเครื่องคอมพิวเตอร์สามารถแบ่งได้ตามหน่วยการทำงานของเครื่องคอมพิวเตอร์ ได้แก่ หน่วยรับข้อมูล หน่วยประมวลผลกลาง หน่วยความจำ และหน่วยแสดงผล</p>

<h3><b>2. ซอร์ฟแวร์</b></h3>
<center>
<img src="img/software.gif" class="w3-round " alt="intro" style="width:60%">
ที่มา(https://web.ku.ac.th/schoolnet/snet1/software/software/table.gif)</center>
<p class="w3-xlarge w3-left">&nbsp;&nbsp;&nbsp;&nbsp; ซอฟต์แวร์ (Software) หมายถึง โปรแกรมหรือชุดคำสั่งที่ควบคุมให้เครื่องคอมพิวเตอร์ทำงานให้ได้ผลลัพธ์ตามที่ต้องการ ซอฟต์แวร์หรือโปรแกรมเขียนขึ้นด้วยภาษาต่างๆ ที่มนุษย์สร้างขึ้น เช่น ภาษาเบสิก (BASIC) ภาษาโคบอล (COBOL) ภาษาปาสคาล (PASCAL) ภาษา HTML (Hypertext Markup Language) เป็นต้น 
     ซอฟต์แวร์แบ่งเป็น 2 ประเภท คือ ซอฟต์แวร์ระบบ (System Software) และซอฟต์แวร์ประยุกต์ (Application Software)</p>

<h3><b>3. ข้อมูล</b></h3>
<p class="w3-xlarge w3-left"> &nbsp;&nbsp;&nbsp;&nbsp;ข้อมูล (Data) หมายถึง ข้อเท็จจริงหรือเหตุการณ์ต่างๆที่เกิดขึ้นในชีวิตประจำวัน ซึ่งอาจเป็นข้อเท็จจริงเกี่ยวกับสถานที่ คน สัตว์ สิ่งของ และอื่นๆ คอมพิวเตอร์สามารถสร้าง จัดเตรียม และจัดเก็บข้อมูลได้หลายประเภทไม่ว่าจะเป็นข้อมูลที่เป็น ข้อความ ตัวเลข รูปภาพ เสียง วิดีโอ และอื่นๆ

     ข้อมูลในคอมพิวเตอร์ แบ่งออกเป็น 2 กลุ่ม คือ<br>
     1. ข้อมูลที่คำนวณได้ (Numeric) ได้แก่ ข้อมูลที่เป็นตัวเลข และจะนำไปใช้ในการคำนวณ<br>
     2. ข้อมูลที่ไม่นำมาคำนวณ (Character) ได้แก่ ข้อมูลที่เป็นตัวอักษรหรือตัวเลข แต่ตัวเลขนั้นไม่สามารถคำนวณได้ เช่น บ้านเลขที่ รหัสประจำตัว เลขที่บัตรประจำตัวประชาชน หมายเลขโทรศัพท์ เป็นต้น <br>

     &nbsp;&nbsp;&nbsp;&nbsp;ข้อมูลทั้ง 2 ประเภท เมื่อถูกนำมาเก็บในเครื่องคอมพิวเตอร์จะอยู่ในรูปแบบของภาษาเครื่อง หรือเลขฐานสองที่ประกอบไปด้วย ตัวเลข 2 ตัว ได้แก่ 0 และ 1 เลขแต่ละตัวจะมีหน่วยเป็น "บิต (Bit)" เช่น 001 ก็จะมี 3 บิต เป็นต้น แต่ถ้าเลขฐานสองรวมกันได้ 8 บิต จะมีค่าเท่ากับ 1 "ไบต์ (Byte)" ซึ่ง บิตและไบต์จะนำมาใช้เป็นรหัสแทนข้อมูลไม่ว่าจะเป็นตัวอักษร ตัวเลข หรือสัญลักษณ์พิเศษ</p>

<h3><b>4. บุคลากร</b></h3>
<p class="w3-xlarge w3-left">&nbsp;&nbsp;&nbsp;&nbsp;บุคลากร (Peopleware) หมายถึง บุคคลที่ปฏิบัติงานในด้านคอมพิวเตอร์ หรือ ผู้ที่เกี่ยวข้องกับระบบสารสนเทศ โดยบุคลากรในระบบสารสนเทศอาจแบ่งได้ดังนี้
      ผู้ใช้งานคอมพิวเตอร์ (user)

      ผู้ดูแลและซ่อมบำรุงเครื่องคอมพิวเตอร์ (Technician)

      โปรแกรมเมอร์ (Programmer)

      นักวิเคราะห์และออกแบบระบบ (System Analyst)

      ผู้บริหารระบบ (System Administrator)</p>

<h3><b>5. ขั้นตอนปฏิบัติงาน</b></h3>
<p class="w3-xlarge w3-left">&nbsp;&nbsp;&nbsp;&nbsp;ขั้นตอนการปฏิบัติงาน คือ ขั้นตอนการทำงานเพื่อให้ได้สารสนเทศที่ต้องการ โดยมีกระบวนการทำงานของระบบสารสนเทศแบ่งเป็น 4 ขั้นตอนหลัก คือ <br>
          
<ul class="w3-ul w3-xlarge"><li>		  1. การนำข้อมูลเข้าสู่ระบบ</li>
     <li> 2. การประมวลผลข้อมูล </li>
          <li> 3. การแสดงผล</li>
          <li> 4. การจัดเก็บข้อมูล</li></ul>
    <p class="w3-xlarge w3-left"> องค์ประกอบของระบบสารสนเทศในส่วนนี้ จัดเป็นกระบวนการที่นำเอาทุกส่วนมาปฏิบัติร่วมกัน ด้วยการวิเคราะห์และออกแบบระบบสารสนเทศที่เหมาะสม และตรงตามความต้องการของผู้ใช้งานมากที่สุด เรียกว่า วงจรการพัฒนาระบบ (SDLC : System Development Lift Cycle) ซึ่งจะแสดงกิจกรรมต่างๆ ภายในระบบสารสนเทศที่ต้องการ โดยเป็นการวิเคราะห์เชิงโครงสร้าง (Structured Analysis) ซึ่งสามารถแบ่งเป็นขั้นตอนต่างๆ ได้ 6 ขั้นตอน คือ
     <ul class="w3-ul w3-xlarge"><li>      1. การวางแผนระบบ (System Planning) </li>
       <li>     2. การวิเคราะห์ระบบ (System Analysis) </li>
        <li>    3. การออกแบบระบบ (System Design) </li>
        <li>   4. การพัฒนาระบบ (System Development) </li>
         <li>   5. การติดตั้งระบบ (System Implementation) </li>
        <li>   6. การดูแลรักษาระบบ (System Maintenance)</p>
